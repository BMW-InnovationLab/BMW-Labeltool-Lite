namespace Robotron.Imagine.Model
{
    /// <summary>
    /// Strucutre of rectangle object to define rectangle objects.
    /// </summary>
    public class Rect
    {
        /// <summary>
        /// X-Coordinate of starting point of rectangle.
        /// </summary>
        public int X { get; set; }

        /// <summary>
        /// Y-Coordinate of starting point of rectangle.
        /// </summary>
        public int Y { get; set; }

        /// <summary>
        /// Width of rectangle.
        /// </summary>
        public int Width { get; set; }

        /// <summary>
        /// Height of rectangle.
        /// </summary>
        public int Height { get; set; }
    }
}
