namespace Robotron.Imagine.Model
{
    /// <summary>
    /// Circle abstraction.
    /// </summary>
    public class Circle
    {
        /// <summary>
        /// X-coordinate of middle of circle.
        /// </summary>
        public int X { get; set; }

        /// <summary>
        /// Y-coordinate of middle of circle.
        /// </summary>
        public int Y { get; set; }

        /// <summary>
        /// Radius of circle.
        /// </summary>
        public int Radius { get; set; }
    }
}
